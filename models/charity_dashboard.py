    # ── deltas (current − prior) for nice arrows in the kanban ──
    delta_total_hours = fields.Float(
        "Δ Total Hours", compute="_compute_deltas",
    )
    delta_elks_unique = fields.Integer(
        "Δ Unique Elks", compute="_compute_deltas",
    )
    delta_helper_unique = fields.Integer(
        "Δ Unique Helpers", compute="_compute_deltas",
    )
    delta_cash_raised = fields.Monetary(
        "Δ $ Raised", currency_field="currency_id",
        compute="_compute_deltas",
    )
    delta_non_cash_raised = fields.Monetary(
        "Δ Non-$ Value", currency_field="currency_id",
        compute="_compute_deltas",
    )
    trend_total_hours = fields.Selection(
        [("up", "Up"), ("flat", "Flat"), ("down", "Down")],
        compute="_compute_deltas", string="Hours Trend",
        search="_search_trend_total_hours",
    )
    trend_cash_raised = fields.Selection(
        [("up", "Up"), ("flat", "Flat"), ("down", "Down")],
        compute="_compute_deltas", string="$ Trend",
        search="_search_trend_cash_raised",
    )

    # ------------------------------------------------------------------
    # Searchable wrappers for the computed trend fields
    # ------------------------------------------------------------------
    def _trend_search(self, field_name, operator, value):
        """Common search implementation for trend_* selection fields.

        Loads all dashboard rows (small set — ~31 categories × N lodge
        years), recomputes the trend in Python, and returns an id-domain.
        """
        if operator not in ("=", "!=", "in", "not in"):
            # Unknown operator: accept all rather than crash.
            return [(1, "=", 1)]
        if isinstance(value, str) or value is False or value is None:
            targets = {value} if value else set()
        else:
            targets = set(value or [])
        records = self.with_context(active_test=False).search([])
        matching = records.filtered(lambda r: r[field_name] in targets)
        if operator in ("=", "in"):
            return [("id", "in", matching.ids or [0])]
        return [("id", "not in", matching.ids or [0])]

    def _search_trend_total_hours(self, operator, value):
        return self._trend_search("trend_total_hours", operator, value)

    def _search_trend_cash_raised(self, operator, value):
        return self._trend_search("trend_cash_raised", operator, value)

    # ------------------------------------------------------------------
    # Prior-year lookups
    # ------------------------------------------------------------------
    @staticmethod
    def _prior_lodge_year_str(lodge_year):
        """Given '2026-2027' return '2025-2026' (or None if unparseable)."""
        if not lodge_year or "-" not in lodge_year:
            return None
        try:
            start, end = lodge_year.split("-")
            return f"{int(start) - 1}-{int(end) - 1}"
        except (TypeError, ValueError):
            return None

    @api.depends("lodge_year", "category_id")
    def _compute_prior_year(self):
        """Look up the prior lodge year's row for the same category."""
        # Batch: collect every (category, prior_year) we need, then query once.
        needed = {}
        for rec in self:
            rec.prior_lodge_year = self._prior_lodge_year_str(rec.lodge_year)
            if rec.prior_lodge_year and rec.category_id:
                key = (rec.category_id.id, rec.prior_lodge_year)
                needed.setdefault(key, self.env[self._name])

        if needed:
            cat_ids = list({k[0] for k in needed})
            years = list({k[1] for k in needed})
            prior_rows = self.search([
                ("category_id", "in", cat_ids),
                ("lodge_year", "in", years),
            ])
            for prior in prior_rows:
                key = (prior.category_id.id, prior.lodge_year)
                if key in needed:
                    needed[key] = prior

        for rec in self:
            key = (rec.category_id.id if rec.category_id else 0,
                   rec.prior_lodge_year)
            prior = needed.get(key) if key[1] else None
            if prior:
                rec.prior_elks_hours = prior.elks_hours
                rec.prior_helper_hours = prior.helper_hours
                rec.prior_total_hours = prior.total_hours
                rec.prior_elks_unique = prior.elks_unique
                rec.prior_helper_unique = prior.helper_unique
                rec.prior_elks_headcount = prior.elks_headcount
                rec.prior_helper_headcount = prior.helper_headcount
                rec.prior_cash_raised = prior.cash_raised
                rec.prior_non_cash_raised = prior.non_cash_raised
            else:
                rec.prior_elks_hours = 0.0
                rec.prior_helper_hours = 0.0
                rec.prior_total_hours = 0.0
                rec.prior_elks_unique = 0
                rec.prior_helper_unique = 0
                rec.prior_elks_headcount = 0
                rec.prior_helper_headcount = 0
                rec.prior_cash_raised = 0.0
                rec.prior_non_cash_raised = 0.0

    @api.depends(
        "total_hours", "prior_total_hours",
        "elks_unique", "prior_elks_unique",
        "helper_unique", "prior_helper_unique",
        "cash_raised", "prior_cash_raised",
        "non_cash_raised", "prior_non_cash_raised",
    )
    def _compute_deltas(self):
        for rec in self:
            rec.delta_total_hours = rec.total_hours - rec.prior_total_hours
            rec.delta_elks_unique = rec.elks_unique - rec.prior_elks_unique
            rec.delta_helper_unique = (
                rec.helper_unique - rec.prior_helper_unique
            )
            rec.delta_cash_raised = rec.cash_raised - rec.prior_cash_raised
            rec.delta_non_cash_raised = (
                rec.non_cash_raised - rec.prior_non_cash_raised
            )
            rec.trend_total_hours = (
                "up" if rec.delta_total_hours > 0.01
                else "down" if rec.delta_total_hours < -0.01
                else "flat"
            )
            rec.trend_cash_raised = (
                "up" if rec.delta_cash_raised > 0.01
                else "down" if rec.delta_cash_raised < -0.01
                else "flat"
            )

    # ------------------------------------------------------------------
    # Website snippet — sudo-safe aggregates only.
    # No individual contributor names, no per-employee records.  Pure
    # category-level rollups so the snippet can render on a fully
    # public page without leaking personal data.
    # ------------------------------------------------------------------
    @api.model
    def _website_totals(self, lodge_year=None):
        """Aggregated charity impact totals for the public website.

        Returns a dict with:
            current_year, prior_year                    — lodge year strings
            totals.{hours,cash,non_cash,people_served}  — current year sums
            prior.{...}                                 — same for prior year
            deltas.{...}                                — current - prior
            pct.{...}                                   — % change (or None)
            cards                                       — list of per-category
                                                          dicts sorted by code
        Safe to call without an authenticated user; the caller should
        sudo() before invoking on the public website.
        """
        lodge_year = lodge_year or _current_lodge_year()
        prior_year = self._prior_lodge_year_str(lodge_year)

        def _aggs(year):
            if not year:
                return {
                    'hours': 0.0, 'cash': 0.0, 'non_cash': 0.0,
                    'people_served': 0,
                }
            rows = self.search([('lodge_year', '=', year)])
            return {
                'hours': sum(rows.mapped('total_hours')),
                'cash': sum(rows.mapped('cash_raised')),
                'non_cash': sum(rows.mapped('non_cash_raised')),
                'people_served': sum(rows.mapped('elks_headcount'))
                                 + sum(rows.mapped('helper_headcount')),
            }

        totals = _aggs(lodge_year)
        prior = _aggs(prior_year)

        def _delta_pct(cur, old):
            if not old:
                return None
            return round(((cur - old) / old) * 100.0, 1)

        deltas = {k: totals[k] - prior[k] for k in totals}
        pct = {k: _delta_pct(totals[k], prior[k]) for k in totals}

        # Per-category cards — sorted by GL code asc.
        cards = []
        for row in self.search([('lodge_year', '=', lodge_year)],
                               order='category_code'):
            prior_row = self.search([
                ('lodge_year', '=', prior_year),
                ('category_id', '=', row.category_id.id),
            ], limit=1) if prior_year else None
            prior_hours = prior_row.total_hours if prior_row else 0.0
            prior_cash = prior_row.cash_raised if prior_row else 0.0
            cards.append({
                'code': row.category_code,
                'name': row.category_name,
                'section': row.category_id.gl_section
                          and dict(row.category_id._fields['gl_section'].selection)
                              .get(row.category_id.gl_section, ''),
                'hours': row.total_hours,
                'cash': row.cash_raised,
                'non_cash': row.non_cash_raised,
                'people_served': row.elks_headcount + row.helper_headcount,
                'activity_count': row.activity_count,
                'prior_hours': prior_hours,
                'prior_cash': prior_cash,
                'delta_hours': row.total_hours - prior_hours,
                'delta_cash': row.cash_raised - prior_cash,
            })

        return {
            'current_year': lodge_year,
            'prior_year': prior_year,
            'totals': totals,
            'prior': prior,
            'deltas': deltas,
            'pct': pct,
            'cards': cards,
            'currency': self.env.company.currency_id,
        }

    # ------------------------------------------------------------------
    # Open dashboard (entry point used by the root menu)
    # ------------------------------------------------------------------
    @api.model
    def action_open_dashboard(self):
        """Return the dashboard act_window pre-filtered to the current
        lodge year via the search field.  Used as the default landing
        for the Elks Charity app.
        """
        action = self.env["ir.actions.act_window"]._for_xml_id(
            "elkscharity.action_charity_dashboard"
        )
        # ir.actions.act_window stores `context` as a Python-source
        # string (e.g. "{'default_x': 1}"). Eval it to a dict before
        # merging — passing a string directly to dict() raises
        # "dictionary update sequence element ... length 1; 2 required".
        raw_ctx = action.get("context") or {}
        if isinstance(raw_ctx, str):
            try:
                base_ctx = safe_eval(raw_ctx) or {}
            except Exception:
                base_ctx = {}
        else:
            base_ctx = dict(raw_ctx)
        base_ctx.update(
            current_lodge_year=_current_lodge_year(),
            # Activate the "Current Lodge Year" named filter so only
            # this year's cards show by default.  User can clear it
            # from the search bar to see all years.
            search_default_filter_current_lodge_year=1,
        )
        action["context"] = base_ctx
        return action

