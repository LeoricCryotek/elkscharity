"""Charity Dashboard — per-category roll-up with prior-year comparison.

A read-only SQL view that aggregates validated timesheet hours,
validated charity-tagged attendance, and confirmed contributions into
ONE row per ``(lodge_year, charity_category)`` pair.  The dashboard
kanban view uses these rows to render widget cards for every Grand
Lodge category, sorted by code (1001 → 9999), with the prior lodge
year's totals computed alongside for year-over-year comparison.

The view is intentionally generated by cross-joining every category
with every lodge year so that categories with **zero** activity still
appear (so the lodge sees what they haven't done yet and is motivated
to act).  Rows are filtered to a specific lodge year at view time via
the default search filter.

Dedup rule (mirrored from project.task._compute_totals): if both a
validated timesheet line and a validated attendance record exist for
the same ``(employee_id, date, task_id)``, the attendance row wins
and the timesheet duplicate is excluded from the totals.
"""
import datetime

from odoo import api, fields, models, tools
from odoo.tools.safe_eval import safe_eval


def _current_lodge_year():
    """Return the current lodge year string, e.g. '2026-2027'.

    Lodge year runs April 1 → March 31.
    """
    today = datetime.date.today()
    if today.month >= 4:
        return f"{today.year}-{today.year + 1}"
    return f"{today.year - 1}-{today.year}"


